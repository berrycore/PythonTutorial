print(10 == 4)  # False
print(10 != 4)  # True
print(10 >= 4)  # True
print(10 <= 4)  # False
print(10 < 4)   # False
print(10 > 4)   # True

x = 20
print( 5 < x < 100)         # True
print( 5 < x and x < 100)   # True

# and
print(True and True)    # True
print(True and False)   # False

# or
print(True or True)     # True
print(True or False)    # True
print(False or False)   # False