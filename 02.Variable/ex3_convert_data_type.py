# convert data type (str)
a = input("input 1st number : ") # if a = "10"
b = input("input 2nd number : ") # if b = "20"
print(a + b)                     # "1020"

# convert data type (str -> int)
a = int(input("input 1st number : ")) # if a = 10
b = int(input("input 2nd number : ")) # if b = 20
print(a + b)                          # 30

# convert data type (int -> str)
a = str(a)                       # 10 -> "10"
b = str(b)                       # 20 -> "20"
print(a + b)                     # "1020"

# convert data type (str -> float)
s = "10"
s = float(s)
print(s)                        # "10" -> 10.0